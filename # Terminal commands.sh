# Terminal commands

cd <folder/file>, it means to change your current location
cd ..; it's used to move up one directory
mkdir <folder>; it's used to make a new directory on the file system
rm-rf; it's used to remove an empty directory
touch <file>; it's used to create a new file
ls; it's used to list files and directories
ls -a; list of all files in the working direcrory including hidden files
mv; it's used to move or rename files or directories
cp; it's used to copy files or directories
code .; it's used to move from git to vscode terminal
git init; it's used to create a new git repository
git add .; it's used to save modifications you've made to your work
git status; it gives us all the information about our currents branch
git commit -m 'caption'; it's used to make a snapshot of the saved changes
git push; it's used to upload local repository content to remote repository if there's already a link between the two
git remote add origin <link>; it's used to connect a local repository with aremote repository
git push -u origin main; it's used to push your commits to your remote repository
git branch <branch name>; it's used to create a new branch which you can name by replacing the word branch with
git checkout <branch name>; it's used when switching to a new branch
git clone <link>; it's used to create a local copy of a remote repository on your local computer
git remote add upstream <link>; it;s used to create an upstrem link between your remote and local repository
git pull upstream; it's used when pulling changes made on your remote repository to immeadiately update your local repository
git remote -v; it's used to display the URLs of the remote repositories linked to your local Git repository.
git mv; it's  used to move or rename files and directories within a Git repository. 